@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(silent=True) or {}
    text = data.get("text", "").strip()
    creator_id = data.get("creator_id", "anonymous")

    if not text:
        return jsonify({"error": "field 'text' is required"}), 400

    llm_score, llm_rationale = get_llm_signal(text)
    stylometric_score = get_stylometric_signal(text)
    confidence = combine_scores(llm_score, stylometric_score)
    attribution = attribution_from_score(confidence)
    label = generate_label(confidence)

    content_id = str(uuid.uuid4())
    record = {
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "text_preview": text[:120],
        "llm_score": llm_score,
        "llm_rationale": llm_rationale,
        "stylometric_score": stylometric_score,
        "confidence": confidence,
        "attribution": attribution,
        "label": label,
        "status": "classified",
        "appeal_reasoning": None,
        "appeal_timestamp": None,
    }
    write_log_entry(record)

    return jsonify(
        {
            "content_id": content_id,
            "attribution": attribution,
            "confidence": confidence,
            "label": label,
            "signals": {
                "llm_score": llm_score,
                "llm_rationale": llm_rationale,
                "stylometric_score": stylometric_score,
            },
        }
    )


@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(silent=True) or {}
    content_id = data.get("content_id")
    creator_reasoning = data.get("creator_reasoning", "").strip()

    if not content_id or not creator_reasoning:
        return jsonify({"error": "fields 'content_id' and 'creator_reasoning' are required"}), 400

    db = get_db()
    row = db.execute(
        "SELECT * FROM audit_log WHERE content_id = ?", (content_id,)
    ).fetchone()
    if row is None:
        return jsonify({"error": "content_id not found"}), 404

    db.execute(
        """
        UPDATE audit_log
        SET status = 'under_review',
            appeal_reasoning = ?,
            appeal_timestamp = ?
        WHERE content_id = ?
        """,
        (creator_reasoning, datetime.now(timezone.utc).isoformat(), content_id),
    )
    db.commit()

    return jsonify(
        {
            "content_id": content_id,
            "status": "under_review",
            "message": "Appeal received and logged. A human reviewer will examine this case.",
        }
    )


@app.route("/log", methods=["GET"])
def log():
    status = request.args.get("status")
    limit = int(request.args.get("limit", 50))
    return jsonify({"entries": get_log_entries(limit=limit, status=status)})


@app.route("/log/<content_id>", methods=["GET"])
def log_single(content_id):
    db = get_db()
    row = db.execute(
        "SELECT * FROM audit_log WHERE content_id = ?", (content_id,)
    ).fetchone()
    if row is None:
        return jsonify({"error": "content_id not found"}), 404
    return jsonify(dict(row))


